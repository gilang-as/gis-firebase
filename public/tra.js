function a(lat, lng, zoom) {
	firebase.database().ref('/maps').on('value', function(snapshot) {
    //console.log(snapshot.val());
	var data = snapshot.val();
	var result = [];

	for (var key in data ){
	   result = result.concat(data [key]);
	}
	$('#activmap-wrapper').activmap({
        places: result,
        icon: 'https://cdn.mapmarker.io/api/v1/pin?icon=fa-star&size=50&hoffset=0&voffset=-1',
        lat: lat, //latitude of the center
        lng: lng, //longitude of the center
        posPanel: 'right',
        showPanel: true,
        radius: 0,
		styles: style,
        cluster: true,
        country: 'id',
        mapType: 'roadmap',
        request: 'large',
        allowMultiSelect: true,
        locationTypes: ['geocode', 'establishment']
    });
	});
};









